package org.cssa.iface.bo;

/**
 * @author ajith
 *
 */
public class Winners {
	
	private int sno;
	private String collegeId;
	private String studentId;
	private String eventId;
	private String winnerStatus;
	private String groupId;
	private float mark;
	
	
	/**
	 * @return the sno
	 */
	public int getSno() {
		return sno;
	}
	/**
	 * @param sno the sno to set
	 */
	public void setSno(int sno) {
		this.sno = sno;
	}
	/**
	 * @return the collegeId
	 */
	public String getCollegeId() {
		return collegeId;
	}
	/**
	 * @param collegeId the collegeId to set
	 */
	public void setCollegeId(String collegeId) {
		this.collegeId = collegeId;
	}
	/**
	 * @return the studentId
	 */
	public String getStudentId() {
		return studentId;
	}
	/**
	 * @param studentId the studentId to set
	 */
	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}
	/**
	 * @return the eventId
	 */
	public String getEventId() {
		return eventId;
	}
	/**
	 * @param eventId the eventId to set
	 */
	public void setEventId(String eventId) {
		this.eventId = eventId;
	}
	/**
	 * @return the winnerStatus
	 */
	public String getWinnerStatus() {
		return winnerStatus;
	}
	/**
	 * @param winnerStatus the winnerStatus to set
	 */
	public void setWinnerStatus(String winnerStatus) {
		this.winnerStatus = winnerStatus;
	}
	/**
	 * @return the groupId
	 */
	public String getGroupId() {
		return groupId;
	}
	/**
	 * @param groupId the groupId to set
	 */
	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}
	/**
	 * @return the mark
	 */
	public float getMark() {
		return mark;
	}
	/**
	 * @param mark the mark to set
	 */
	public void setMark(float mark) {
		this.mark = mark;
	}
	
	

}
